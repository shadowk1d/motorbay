export function getModelsForBrand(brand?: string) {
  if (!brand) {
    const set = new Set<string>();
    Object.values(BRAND_MODELS).forEach((models) => models.forEach((model) => set.add(model)));
    return Array.from(set).sort((a, b) => a.localeCompare(b));
  }

  const normalizedBrand = CAR_BRANDS.find((item) => item.toLowerCase() === brand.toLowerCase());
  return normalizedBrand ? [...BRAND_MODELS[normalizedBrand]].sort((a, b) => a.localeCompare(b)) : [];
}

